class Font(Window):
	'''Draws some text in a bitmapped font with glBitmap() and other pixel routines. Also demonstrates the use of display lists.'''

	def __init__(self):
		super(Font, self).__init__("font.c", "Font", 300, 100)
		glShadeModel(GL_FLAT)
		self.makeRasterFont()

	def display(self):
		glClear(GL_COLOR_BUFFER_BIT)
		glColor3f(1, 1, 1)
		glRasterPos2i(20, 60)
		self.stringRender("THE QUICK BROWN FOX JUMPS")
		glRasterPos2i(20, 40)
		self.stringRender("OVER A LAZY DOG")
		glFlush()

	def makeRasterFont(self):
		glPixelStorei(GL_UNPACK_ALIGNMENT, 1)
		self.fontOffset = glGenLists(128)
		for i in range(26):
			j = ord('A') + i
			glNewList(self.fontOffset + j, GL_COMPILE)
			glBitmap(8, 13, 0, 2, 10, 0, letters[i])
			glEndList()
		glNewList(self.fontOffset + ord(' '), GL_COMPILE)
		glBitmap(8, 13, 0, 2, 10, 0, space)
		glEndList()

	def stringRender(self, s):
		'''You've got to call makeRasterFont() before you start making calls to stringRender().'''
		glPushAttrib(GL_LIST_BIT)
		glListBase(self.fontOffset)
		glCallLists(len(s), GL_UNSIGNED_BYTE, s)
		glPopAttrib()


if __name__ == '__main__':
	Font().run()
